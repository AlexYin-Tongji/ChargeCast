"""Prepare charge prediction datasets.

Key behaviors:
1) True gate flow matching uses train-first, then test fallback.
2) Predicted gate flow is generated by the trained flow model checkpoint by default.
3) Negative predicted gate flow values are clipped to 0 by default.
"""

from __future__ import annotations

import argparse
import json
import os
import sys
from typing import Dict, List, Optional

import numpy as np
import pandas as pd
import torch

sys.path.append(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

import flowConfig.config as config
from flowScripts.predict import load_model
from src.data.alignment import build_canonical_nodes
from src.data.flow_data import FlowDataLoader


MERGE_KEYS = ["node_id", "date", "hour_code"]


def _ensure_dir(path: str) -> None:
    os.makedirs(path, exist_ok=True)


def _to_str(df: pd.DataFrame, cols: List[str]) -> pd.DataFrame:
    for c in cols:
        if c in df.columns:
            df[c] = df[c].astype(str)
    return df


def _to_int_hour(df: pd.DataFrame, col: str = "hour_code") -> pd.DataFrame:
    if col in df.columns:
        df[col] = pd.to_numeric(df[col], errors="coerce").fillna(0).astype(int)
    return df


def _build_time_col(df: pd.DataFrame) -> pd.Series:
    if "slice_start" in df.columns:
        t = pd.to_datetime(df["slice_start"], errors="coerce")
        if t.notna().any():
            return t

    if "date" in df.columns and "hour_code" in df.columns:
        return pd.to_datetime(df["date"].astype(str), errors="coerce") + pd.to_timedelta(
            pd.to_numeric(df["hour_code"], errors="coerce").fillna(0).astype(int), unit="h"
        )

    if "date" in df.columns:
        return pd.to_datetime(df["date"], errors="coerce")

    return pd.Series(np.arange(len(df)), index=df.index)


def _read_flow_raw(path: str) -> pd.DataFrame:
    df = pd.read_csv(path)
    required = {"station_id", "date", "hour_code", "nev_flow"}
    missing = [c for c in required if c not in df.columns]
    if missing:
        raise ValueError(f"Flow csv {path} missing required columns: {missing}")

    out = df[["station_id", "date", "hour_code", "nev_flow"]].copy()
    out["node_id"] = out["station_id"].astype(str)
    out["date"] = out["date"].astype(str)
    out = _to_int_hour(out, "hour_code")
    out["nev_flow"] = pd.to_numeric(out["nev_flow"], errors="coerce").fillna(0.0)
    return out[["node_id", "date", "hour_code", "nev_flow"]]


def _aggregate_flow(df: pd.DataFrame, value_col: str, out_col: str) -> pd.DataFrame:
    return (
        df.groupby(MERGE_KEYS, as_index=False)[value_col]
        .sum()
        .rename(columns={value_col: out_col})
    )


def _merge_true_flow_train_then_test(base_df: pd.DataFrame, flow_train: pd.DataFrame, flow_test: pd.DataFrame):
    train_true = _aggregate_flow(flow_train, "nev_flow", "gate_nev_flow_train")
    test_true = _aggregate_flow(flow_test, "nev_flow", "gate_nev_flow_test")

    out = base_df.merge(train_true, on=MERGE_KEYS, how="left")
    out = out.merge(test_true, on=MERGE_KEYS, how="left")

    out["gate_nev_flow_true"] = out["gate_nev_flow_train"].combine_first(out["gate_nev_flow_test"])

    true_source = np.where(
        out["gate_nev_flow_train"].notna(),
        "train",
        np.where(out["gate_nev_flow_test"].notna(), "test", "missing"),
    )
    out["gate_true_source"] = true_source
    return out


def _build_model_pred_table(
    model,
    checkpoint: Dict[str, object],
    flow_train: pd.DataFrame,
    flow_test: pd.DataFrame,
    charge_nodes: List[str],
    batch_size: int,
    clip_negative_pred: bool,
) -> pd.DataFrame:
    topo_order = checkpoint["topo_order"]
    node_to_idx = {n: i for i, n in enumerate(topo_order)}

    target_nodes = [n for n in sorted(set(charge_nodes)) if n in node_to_idx]
    if not target_nodes:
        return pd.DataFrame(columns=["node_id", "date", "hour_code", "gate_nev_flow_pred"])

    flow_all = pd.concat([flow_train, flow_test], axis=0, ignore_index=True)
    flow_all = _aggregate_flow(flow_all, "nev_flow", "flow")

    # Build sorted timeline from available flow records.
    timeline = flow_all[["date", "hour_code"]].drop_duplicates().copy()
    timeline["_time"] = pd.to_datetime(timeline["date"], errors="coerce") + pd.to_timedelta(
        timeline["hour_code"], unit="h"
    )
    timeline = timeline.sort_values(["_time", "date", "hour_code"]).reset_index(drop=True)

    if len(timeline) <= config.SEQ_LEN:
        return pd.DataFrame(columns=["node_id", "date", "hour_code", "gate_nev_flow_pred"])

    flow_pivot = flow_all.pivot_table(index=["date", "hour_code"], columns="node_id", values="flow", aggfunc="sum")
    timeline_index = pd.MultiIndex.from_frame(timeline[["date", "hour_code"]])
    flow_pivot = flow_pivot.reindex(index=timeline_index, fill_value=0.0)
    flow_pivot = flow_pivot.reindex(columns=topo_order, fill_value=0.0)

    data = flow_pivot.to_numpy(dtype=np.float32)

    norm_stats = checkpoint.get("norm_stats")
    if norm_stats is None:
        raise ValueError("Checkpoint missing norm_stats for flow model prediction.")

    mean = np.asarray(norm_stats["mean"], dtype=np.float32)
    std = np.asarray(norm_stats["std"], dtype=np.float32)

    if len(mean) != data.shape[1] or len(std) != data.shape[1]:
        raise ValueError(
            "Flow model checkpoint norm_stats size mismatch with topo_order. "
            f"mean={len(mean)} std={len(std)} nodes={data.shape[1]}"
        )

    data_norm = (data - mean) / (std + 1e-8)

    # Samples predict time index t from history [t-seq_len, t).
    seq_len = config.SEQ_LEN
    sample_count = len(data_norm) - seq_len

    X = np.stack([data_norm[i : i + seq_len] for i in range(sample_count)], axis=0)
    target_indices = np.arange(seq_len, len(data_norm))
    hours = timeline.iloc[target_indices]["hour_code"].to_numpy(dtype=np.float32).reshape(-1, 1)

    preds = []
    model.eval()

    with torch.no_grad():
        for start in range(0, sample_count, batch_size):
            end = min(start + batch_size, sample_count)
            xb = torch.tensor(X[start:end], dtype=torch.float32, device=next(model.parameters()).device)
            hb = torch.tensor(hours[start:end], dtype=torch.float32, device=next(model.parameters()).device)
            out = model(xb, hb)  # [B, 1, N]
            preds.append(out[:, 0, :].cpu().numpy())

    pred_norm = np.concatenate(preds, axis=0)
    pred = pred_norm * std[None, :] + mean[None, :]

    if clip_negative_pred:
        pred = np.clip(pred, a_min=0.0, a_max=None)

    target_node_indices = [node_to_idx[n] for n in target_nodes]
    pred_selected = pred[:, target_node_indices]

    pred_wide = pd.DataFrame(pred_selected, columns=target_nodes)
    pred_wide["date"] = timeline.iloc[target_indices]["date"].values
    pred_wide["hour_code"] = timeline.iloc[target_indices]["hour_code"].values

    pred_long = pred_wide.melt(
        id_vars=["date", "hour_code"],
        value_vars=target_nodes,
        var_name="node_id",
        value_name="gate_nev_flow_pred",
    )

    pred_long = _to_str(pred_long, ["node_id", "date"])
    pred_long = _to_int_hour(pred_long, "hour_code")
    return pred_long


def prepare_dataset(
    charge_path: str,
    flow_train_path: str,
    flow_test_path: str,
    pred_flow_path: Optional[str],
    checkpoint_path: str,
    output_dir: str,
    train_ratio: float,
    use_canonical_filter: bool,
    clip_negative_pred: bool,
    use_model_pred: bool,
    model_pred_batch_size: int,
    strict_model_pred: bool,
    final_train_path: str,
    final_test_path: str,
) -> Dict[str, object]:
    _ensure_dir(output_dir)

    # 1) Load charge data.
    charge_df = pd.read_csv(charge_path)
    required_charge_cols = {"node_id", "nev_flow", "power"}
    missing_charge = [c for c in required_charge_cols if c not in charge_df.columns]
    if missing_charge:
        raise ValueError(f"charge csv missing required columns: {missing_charge}")

    charge_df = _to_str(charge_df, ["node_id", "date"])
    charge_df = _to_int_hour(charge_df, "hour_code")

    # 2) Canonical nodes (same as flow training) for optional filtering.
    loader = FlowDataLoader(config.DATA_DIR)
    flow_train_pivot = loader.load_flow_data("train.csv")
    flow_test_pivot = loader.load_flow_data("test.csv")
    adj_df = loader.load_adjacency("adj.csv")
    canonical_nodes, canonical_stats = build_canonical_nodes(
        adj_df,
        flow_train_pivot,
        flow_test_pivot,
        enabled=config.NODE_ALIGNMENT.get("enabled", True),
    )
    canonical_set = set(canonical_nodes)

    charge_rows_before_filter = len(charge_df)
    charge_nodes_before = sorted(charge_df["node_id"].dropna().unique().tolist())

    if use_canonical_filter:
        charge_df = charge_df[charge_df["node_id"].isin(canonical_set)].copy()

    charge_rows_after_filter = len(charge_df)
    charge_nodes_after = sorted(charge_df["node_id"].dropna().unique().tolist())

    # 3) Build true flow with train->test fallback.
    flow_train_raw = _read_flow_raw(flow_train_path)
    flow_test_raw = _read_flow_raw(flow_test_path)

    base_df = _merge_true_flow_train_then_test(charge_df, flow_train_raw, flow_test_raw)

    # 4) Predicted flow input: model prediction (default) or external file or true flow.
    used_predicted_flow = False
    pred_source = "none"
    model_pred_error = ""

    if use_model_pred:
        try:
            device = config.get_device()
            model, checkpoint = load_model(checkpoint_path, device)
            pred_prepared = _build_model_pred_table(
                model=model,
                checkpoint=checkpoint,
                flow_train=flow_train_raw,
                flow_test=flow_test_raw,
                charge_nodes=charge_nodes_after if charge_nodes_after else charge_nodes_before,
                batch_size=model_pred_batch_size,
                clip_negative_pred=clip_negative_pred,
            )

            base_df = base_df.merge(pred_prepared, on=MERGE_KEYS, how="left")
            base_df["gate_nev_flow_input"] = base_df["gate_nev_flow_pred"].combine_first(base_df["gate_nev_flow_true"])
            used_predicted_flow = True
            pred_source = "flow_model"
        except Exception as exc:
            model_pred_error = str(exc)
            if strict_model_pred:
                raise RuntimeError(
                    "Flow model prediction failed and --strict-model-pred is enabled."
                ) from exc
            base_df["gate_nev_flow_input"] = base_df["gate_nev_flow_true"]
            pred_source = "model_failed_fallback_true"

    elif pred_flow_path:
        pred_df = pd.read_csv(pred_flow_path)
        cands = {
            "node_id": ["node_id", "station_id"],
            "date": ["date"],
            "hour_code": ["hour_code", "hour", "hour_idx"],
            "pred": ["pred_nev_flow", "flow_pred", "y_pred", "nev_flow_pred", "nev_flow"],
        }

        out_cols: Dict[str, str] = {}
        for k, options in cands.items():
            for opt in options:
                if opt in pred_df.columns:
                    out_cols[k] = opt
                    break

        missing = [k for k in ["node_id", "date", "hour_code", "pred"] if k not in out_cols]
        if missing:
            raise ValueError(
                f"Predicted flow file missing columns for: {missing}. available={list(pred_df.columns)}"
            )

        pred_prepared = pred_df.rename(
            columns={
                out_cols["node_id"]: "node_id",
                out_cols["date"]: "date",
                out_cols["hour_code"]: "hour_code",
                out_cols["pred"]: "gate_nev_flow_pred",
            }
        )[["node_id", "date", "hour_code", "gate_nev_flow_pred"]]

        pred_prepared = _to_str(pred_prepared, ["node_id", "date"])
        pred_prepared = _to_int_hour(pred_prepared, "hour_code")
        pred_prepared = (
            pred_prepared.groupby(MERGE_KEYS, as_index=False)["gate_nev_flow_pred"]
            .mean()
        )

        if clip_negative_pred:
            pred_prepared["gate_nev_flow_pred"] = pred_prepared["gate_nev_flow_pred"].clip(lower=0)

        base_df = base_df.merge(pred_prepared, on=MERGE_KEYS, how="left")
        base_df["gate_nev_flow_input"] = base_df["gate_nev_flow_pred"].combine_first(base_df["gate_nev_flow_true"])
        used_predicted_flow = True
        pred_source = "external_pred_file"

    else:
        base_df["gate_nev_flow_input"] = base_df["gate_nev_flow_true"]

    # 5) Rename charge fields for explicit semantics.
    # `charge_power_total` is raw total power from source data.
    base_df = base_df.rename(columns={"nev_flow": "charge_nev_flow", "power": "charge_power_total"})
    if "gate_nev_flow_pred" not in base_df.columns:
        base_df["gate_nev_flow_pred"] = np.nan

    # 6) Time-order split 8:2.
    base_df["_time"] = _build_time_col(base_df)
    base_df = base_df.sort_values(["_time", "node_id"]).reset_index(drop=True)

    n_total = len(base_df)
    n_train = int(n_total * train_ratio)
    n_train = min(max(n_train, 0), n_total)

    train_df = base_df.iloc[:n_train].copy()
    test_df = base_df.iloc[n_train:].copy()

    drop_cols = ["_time"]
    train_df = train_df.drop(columns=[c for c in drop_cols if c in train_df.columns])
    test_df = test_df.drop(columns=[c for c in drop_cols if c in test_df.columns])
    all_df = base_df.drop(columns=[c for c in drop_cols if c in base_df.columns])

    all_path = os.path.join(output_dir, "charge_model_all.csv")
    train_path = os.path.join(output_dir, "charge_model_train.csv")
    test_path = os.path.join(output_dir, "charge_model_test.csv")
    report_path = os.path.join(output_dir, "dataset_report.json")

    all_df.to_csv(all_path, index=False, encoding="utf-8-sig")
    train_df.to_csv(train_path, index=False, encoding="utf-8-sig")
    test_df.to_csv(test_path, index=False, encoding="utf-8-sig")

    # 7) Build required final format files.
    # train.csv: use predicted gate flow
    # test.csv: use true gate flow
    required_cols = ["hour_code", "week_code", "station_id", "node_id", "charge_power_total"]
    missing_required = [c for c in required_cols if c not in all_df.columns]
    if missing_required:
        raise ValueError(
            f"Missing required columns for final export: {missing_required}. "
            f"Available: {list(all_df.columns)}"
        )

    def _build_final(df: pd.DataFrame, flow_col: str) -> pd.DataFrame:
        out = df.copy()
        out[flow_col] = pd.to_numeric(out[flow_col], errors="coerce")
        out["charge_nev_flow"] = pd.to_numeric(out["charge_nev_flow"], errors="coerce")
        out["charge_power_total"] = pd.to_numeric(out["charge_power_total"], errors="coerce")

        out = out.dropna(subset=[flow_col, "charge_nev_flow", "charge_power_total"])
        out = out[out[flow_col] > 0]
        out = out[out["charge_nev_flow"] > 0]

        out["charge_flow_ratio"] = out["charge_nev_flow"] / out[flow_col]
        out["charge_power"] = out["charge_power_total"] / out["charge_nev_flow"]
        out = out.replace([np.inf, -np.inf], np.nan).dropna(subset=["charge_flow_ratio"])
        out = out[out["charge_flow_ratio"] > 0]
        out = out.replace([np.inf, -np.inf], np.nan).dropna(subset=["charge_power"])
        out = out[out["charge_power"] > 0]

        return out[["hour_code", "week_code", "station_id", "node_id", "charge_flow_ratio", "charge_power"]]

    train_final = _build_final(train_df, "gate_nev_flow_pred")
    test_final = _build_final(test_df, "gate_nev_flow_true")

    _ensure_dir(os.path.dirname(final_train_path))
    _ensure_dir(os.path.dirname(final_test_path))
    train_final.to_csv(final_train_path, index=False, encoding="utf-8-sig")
    test_final.to_csv(final_test_path, index=False, encoding="utf-8-sig")

    report = {
        "paths": {
            "charge_path": charge_path,
            "flow_train_path": flow_train_path,
            "flow_test_path": flow_test_path,
            "pred_flow_path": pred_flow_path,
            "checkpoint_path": checkpoint_path,
            "all_path": all_path,
            "train_path": train_path,
            "test_path": test_path,
            "final_train_path": final_train_path,
            "final_test_path": final_test_path,
        },
        "settings": {
            "train_ratio": train_ratio,
            "use_canonical_filter": use_canonical_filter,
            "clip_negative_pred": clip_negative_pred,
            "use_model_pred": use_model_pred,
            "model_pred_batch_size": model_pred_batch_size,
            "used_predicted_flow": used_predicted_flow,
            "pred_source": pred_source,
            "strict_model_pred": strict_model_pred,
        },
        "counts": {
            "canonical_nodes": len(canonical_nodes),
            "charge_nodes_before_filter": len(charge_nodes_before),
            "charge_nodes_after_filter": len(charge_nodes_after),
            "charge_rows_before_filter": int(charge_rows_before_filter),
            "charge_rows_after_filter": int(charge_rows_after_filter),
            "rows_all": int(len(all_df)),
            "rows_train": int(len(train_df)),
            "rows_test": int(len(test_df)),
            "final_rows_train": int(len(train_final)),
            "final_rows_test": int(len(test_final)),
            "gate_true_from_train_rows": int((all_df.get("gate_true_source") == "train").sum()) if len(all_df) else 0,
            "gate_true_from_test_rows": int((all_df.get("gate_true_source") == "test").sum()) if len(all_df) else 0,
            "gate_true_missing_rows": int((all_df.get("gate_true_source") == "missing").sum()) if len(all_df) else 0,
        },
        "missing": {
            "charge_nodes_not_in_canonical_preview": sorted(list(set(charge_nodes_before) - canonical_set))[:20],
            "gate_pred_missing_rows": int(all_df["gate_nev_flow_pred"].isna().sum()) if "gate_nev_flow_pred" in all_df.columns else 0,
            "gate_input_missing_rows": int(all_df["gate_nev_flow_input"].isna().sum()) if "gate_nev_flow_input" in all_df.columns else 0,
            "model_pred_error": model_pred_error,
        },
        "flow_canonical_stats": {
            "adj_nodes": canonical_stats.get("adj_nodes", 0),
            "train_nodes": canonical_stats.get("train_nodes", 0),
            "test_nodes": canonical_stats.get("test_nodes", 0),
            "canonical_nodes": canonical_stats.get("canonical_nodes", 0),
        },
    }

    with open(report_path, "w", encoding="utf-8") as f:
        json.dump(report, f, ensure_ascii=False, indent=2)

    report["report_path"] = report_path
    return report


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Prepare charge prediction datasets.")
    parser.add_argument("--charge-path", default="data/charge/charge.csv")
    parser.add_argument("--flow-train-path", default="data/flow/train.csv")
    parser.add_argument("--flow-test-path", default="data/flow/test.csv")
    parser.add_argument("--pred-flow-path", default="", help="Optional external predicted flow csv path.")
    parser.add_argument(
        "--checkpoint-path",
        default=os.path.join(config.MODEL_DIR, config.ENDOGENOUS_TRAIN["checkpoint_name"]),
        help="Flow model checkpoint path used for model-based predicted flow.",
    )
    parser.add_argument("--output-dir", default="chargePrediction/data")
    parser.add_argument("--train-ratio", type=float, default=0.8)
    parser.add_argument(
        "--use-canonical-filter",
        action="store_true",
        help="Enable canonical node filtering (adj∩train∩test). Default off for sample compatibility.",
    )
    parser.add_argument(
        "--no-clip-negative-pred",
        action="store_true",
        help="Do not clip negative predicted flow values.",
    )
    parser.add_argument(
        "--no-model-pred",
        action="store_true",
        help="Disable model-based predicted flow generation.",
    )
    parser.add_argument(
        "--model-pred-batch-size",
        type=int,
        default=256,
        help="Batch size for model-based flow prediction.",
    )
    parser.add_argument(
        "--strict-model-pred",
        action="store_true",
        help="Fail immediately if model-based flow prediction cannot be generated.",
    )
    parser.add_argument("--final-train-path", default="data/charge/train.csv")
    parser.add_argument("--final-test-path", default="data/charge/test.csv")
    return parser.parse_args()


def main() -> None:
    args = parse_args()

    report = prepare_dataset(
        charge_path=args.charge_path,
        flow_train_path=args.flow_train_path,
        flow_test_path=args.flow_test_path,
        pred_flow_path=args.pred_flow_path.strip() or None,
        checkpoint_path=args.checkpoint_path,
        output_dir=args.output_dir,
        train_ratio=args.train_ratio,
        use_canonical_filter=args.use_canonical_filter,
        clip_negative_pred=not args.no_clip_negative_pred,
        use_model_pred=not args.no_model_pred,
        model_pred_batch_size=args.model_pred_batch_size,
        strict_model_pred=args.strict_model_pred,
        final_train_path=args.final_train_path,
        final_test_path=args.final_test_path,
    )

    print("=" * 72)
    print("Charge Modeling Dataset Prepared")
    print("=" * 72)
    print(f"rows_all:   {report['counts']['rows_all']}")
    print(f"rows_train: {report['counts']['rows_train']}")
    print(f"rows_test:  {report['counts']['rows_test']}")
    print(f"final_rows_train: {report['counts']['final_rows_train']}")
    print(f"final_rows_test:  {report['counts']['final_rows_test']}")
    print(f"canonical_nodes: {report['counts']['canonical_nodes']}")
    print(f"charge_rows_before_filter: {report['counts']['charge_rows_before_filter']}")
    print(f"charge_rows_after_filter:  {report['counts']['charge_rows_after_filter']}")
    print(f"gate_true_from_train_rows: {report['counts']['gate_true_from_train_rows']}")
    print(f"gate_true_from_test_rows:  {report['counts']['gate_true_from_test_rows']}")
    print(f"gate_true_missing_rows:    {report['counts']['gate_true_missing_rows']}")
    print(f"gate_pred_missing_rows:    {report['missing']['gate_pred_missing_rows']}")
    print(f"gate_input_missing_rows:   {report['missing']['gate_input_missing_rows']}")
    if report["missing"].get("model_pred_error"):
        print("model_pred_error:         present (see dataset_report.json)")
    print(f"pred_source: {report['settings']['pred_source']}")
    print(f"final_train_path: {report['paths']['final_train_path']}")
    print(f"final_test_path:  {report['paths']['final_test_path']}")
    print(f"report_path: {report['report_path']}")
    print("=" * 72)


if __name__ == "__main__":
    main()
